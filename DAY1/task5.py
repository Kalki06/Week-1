num1 = int(input("Enter your first number : ")) #here we are taking input first number
num2 = int(input("Enter your second number : ")) #taking second number input from user
sum = num1 + num2  # calculating sum of num 1 and 2
sub = num1 - num2  # calculating difference of num 1 and 2
pro = num1 * num2  # calculating product of num 1 and 2
print(f"Sum of {num1} and {num2} is {sum}") # printing sum of num 1 and 2
print(f"Difference of {num1} and {num2} is {sub}") # printing difference of num 1 and 2
print(f"Product of {num1} and {num2} is {pro}") # printing product of num 1 and 2